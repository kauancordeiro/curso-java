package entites;

public class Pessoas {
	
	private Double height;
	private char gen;
	
	public Pessoas() {
		
	}
	
	public Pessoas(Double height, char gen) {
		this.height = height;
		this.gen = gen;
	}

	public Double getHeight() {
		return height;
	}

	public void setHeight(Double height) {
		this.height = height;
	}

	public char getGen() {
		return gen;
	}

	public void setGen(char gen) {
		this.gen = gen;
	}
	
	
	
	
	
}
